private fun String.formatResourceString(vararg args: Any): String =
    java.lang.String.format(java.util.Locale.ROOT, this, *args)

private fun DesktopUiLanguage.applyAsDefaultLocale() {
    val locale = when (this) {
        DesktopUiLanguage.ENGLISH -> JavaLocale.ENGLISH
        DesktopUiLanguage.ZH_TW -> JavaLocale.TRADITIONAL_CHINESE
    }
    if (JavaLocale.getDefault() != locale) {
        JavaLocale.setDefault(locale)
    }
}
